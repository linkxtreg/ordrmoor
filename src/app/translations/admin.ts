function getNested(obj: TranslationTree, path: string): string | undefined {
  const parts = path.split('.');
  let current: string | TranslationTree | undefined = obj;
  for (const p of parts) {
    if (current == null || typeof current !== 'object') return undefined;
    current = (current as TranslationTree)[p];
  }
  return typeof current === 'string' ? current : undefined;
}

export function getAdminT(lang: AdminLang) {
  const strings = adminTranslations[lang];
  return (key: string, params?: Record<string, string>): string => {
    let s = getNested(strings, key) ?? key;
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
      });
    }
    return s;
  };
}
